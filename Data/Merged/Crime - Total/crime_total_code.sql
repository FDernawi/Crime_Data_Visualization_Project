SELECT 
    COALESCE(c.tract, t.tract) AS tract,
    c.offense,
    c.lat,
    c.long,
    t.total,
    t.total_no,
    t.total_hs,
    t.total_col,
    t.white,
    t.white_no,
    t.white_hs,
    t.white_col,
    t.black,
    t.black_no,
    t.black_hs,
    t.black_col,
    t.aian,
    t.aian_no,
    t.aian_hs,
    t.aian_col,
    t.asian,
    t.asian_no,
    t.asian_hs,
    t.asian_col,
    t.nhpi,
    t.nhpi_no,
    t.nhpi_hs,
    t.nhpi_col,
    t.other,
    t.other_no,
    t.other_hs,
    t.other_col,
    t.multi,
    t.multi_no,
    t.multi_hs,
    t.multi_col,
    t.hisp,
    t.hisp_no,
    t.hisp_hs,
    t.hisp_col
FROM crime_data c
FULL JOIN total_pop_data t 
ON c.tract = t.tract
ORDER BY COALESCE(c.tract, t.tract), c.offense;